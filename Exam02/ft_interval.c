#include <unistd.h>

void	print_interval(char *c)
{
	int	i;

	i = 0;
	while(c[i] != '\0')
	{
		if ((i % 5 == 0) && (i != 0))
		{
			write(1, "3", 1);
		}
		else if ((i % 3 == 0) && (i != 0))
		{
			write(1, "5", 1);
		}
		else if ((i % 3 == 0) && (i % 5 ==0) && (i !=0))
		{
			write(1, "5", 1);
		}
		else
			write(1, &c[i], 1);
		i++;
	}
	write(1, "\n", 1);
}

int	main(void)
{
	ft_interval("abcdef");
}
